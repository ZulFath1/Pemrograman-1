import math
putaran=5
jarak=14
jari=jarak/(putaran*2*math.pi)
print("Diketahui : ")
print(f"Pak Dengklek mengelilingi taman = {putaran} Putaran")
print(f"Jarak tempuh Pak Dengklek = {jarak} Kilometer")
print("\nJawaban : ")
print(f"Jari-jari taman yang dikelilingi Pak Dengklek adalah {jari:.2f} Kilometer")